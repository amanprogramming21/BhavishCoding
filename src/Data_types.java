public class Data_types {
    public static void main(String[] args) {
        int a;
        System.out.println("Increment");
        for(a=0;a<51;a++) {
            System.out.println(a);
        }
        System.out.println("\nDecrement");
        for (a=51;a>0;a--) {
            System.out.println(a);
        }
    }
}
